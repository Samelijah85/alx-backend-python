#!/usr/bin/env python3
"""Implements a type-annotated function that adds two floats"""


def add(a: float, b: float) -> float:
    """
    Takes a float a and a float b as arguments and add them

    Args:
        a (float): The first float
        b (float): The second float

    Return:
        float: The sum of the two floats
    """
    return a + b
