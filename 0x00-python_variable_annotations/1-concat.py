#!/usr/bin/env python3
"""Implements a type-annotated function that concatenates two strings"""


def concat(str1: str, str2: str) -> str:
    """
    Takes a string str1 and a string str2 as arguments and concatenate

    Args:
        str1 (str): The first string
        str2 (str): The second string

    Returns:
        str: The concatenated string
    """
    return str1 + str2
